<li><b>	Do I have to pay an advisor?</b><ul >
<li>
It is free to send messages to an advisor on the platform. How you choose to engage with an advisor offline, either through goodwill, or though equity/fee, is between you and the advisor and FounderMates have no say in that. As a platform we encourage you to find right advisors and then take the relationship offline to get tangible benefits.

</li>
</ul>
</br>

<li><b>I am not from the UK or India. Can I join?</b><ul >
<li>
No. At this point, we are open to advisors and entrepreneurs in the UK and India. But we plan to scale up and we will update you when we launch in other countries.
</li>
</ul>
</br>


<li><b>	How is this different from LinkedIn, Social Media, Clarity.fm or other mentoring bodies?</b><ul >
<li>
LinkedIn is for all kinds of professionals. Finding an advisor who is willing to help you and contacting him is not easy as LinkedIn prevents messages from 2nd degree and 3rd degree connections. Social Media channels have limitations in finding advisors with right skills, location and interest to help. In Clarity.fm finding a free advisor is difficult. It is highly mobile focused and does not incorporate local component like we do. Mentoring bodies and groups usually do not let you connect to advisors directly. They also ask for a fee upfront.

</li>
</ul>
</br>

<li><b>		I usually attend networking events to find advisors. How will this benefit me?</b><ul >
<li>
We have found out that networking events although great is not all inclusive. You will not get a chance to effectively network with every advisor in an event. And every advisor cannot make it to every event. Thus an online channel that helps you to find advisors can act as a great compliment to networking events.

</li>
</ul>
</br>

<li><b>	How can I find and connect to advisors on FounderMates?</b><ul >
<li>
You can search an advisor based on experience, skills, seeing his LinkedIn profile and location and then message him via the email feature within the platform.
</li>
</ul>
</br>

<li><b>	At what stage of business do I need to be to join FounderMates?</b><ul >
<li>
Anyone from a startup idea to high growth SME can join FounderMates.
</li>
</ul>
</br>

